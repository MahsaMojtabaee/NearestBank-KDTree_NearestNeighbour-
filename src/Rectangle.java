public class Rectangle {
    String name;
    double x_min, x_max, y_min, y_max;
    Rectangle(String name, double x_min, double x_max, double y_min, double y_max){
        this.name = name;
        this.x_min = x_min;
        this.y_min = y_min;
        this.x_max = x_max;
        this.y_max = y_max;
    }
}

